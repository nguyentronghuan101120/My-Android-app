                    JetpackComposeRoute()
                }
            }
        }
    }
}

@Composable
fun JetpackComposeRoute() {
    val navController = rememberNavController()

    NavHost(navController = navController, startDestination = Route.Login.route) {
        composable(Route.Login.route) {
            LoginPage(
                onPress = {
                },
            )
        }

        composable(Route.Home.route) {
            HomeScreen()
        }
    }
}

